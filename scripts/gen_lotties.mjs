const W = 200, H = 200;
const FPS = 30, FRAMES = 60; // 2s bounce loop

// --- helpers -----------------------------------------------------------------

function c(r, g, b, a = 1) {
  return [r / 255, g / 255, b / 255, a];
}

// Required transform at the end of every Lottie group's `it` array
const TR = {
  ty: 'tr', nm: '',
  p: { a: 0, k: [0, 0] }, a: { a: 0, k: [0, 0] },
  s: { a: 0, k: [100, 100] }, r: { a: 0, k: 0 },
  o: { a: 0, k: 100 }, sk: { a: 0, k: 0 }, sa: { a: 0, k: 0 },
};

function el(cx, cy, w, h, color) {
  return {
    ty: 'gr', nm: '', np: 3, cix: 2, bm: 0, ix: 1, mn: '', hd: false,
    it: [
      { ty: 'el', nm: '', p: { a: 0, k: [cx, cy] }, s: { a: 0, k: [w, h] } },
      { ty: 'fl', nm: '', c: { a: 0, k: color }, o: { a: 0, k: 100 }, r: 1 },
      TR,
    ],
  };
}

function tri(x1, y1, x2, y2, x3, y3, color) {
  return {
    ty: 'gr', nm: '', np: 3, cix: 2, bm: 0, ix: 1, mn: '', hd: false,
    it: [
      {
        ty: 'sh', nm: '',
        ks: { a: 0, k: { v: [[x1, y1], [x2, y2], [x3, y3]], i: [[0,0],[0,0],[0,0]], o: [[0,0],[0,0],[0,0]], c: true } },
      },
      { ty: 'fl', nm: '', c: { a: 0, k: color }, o: { a: 0, k: 100 }, r: 1 },
      TR,
    ],
  };
}

function rc(cx, cy, w, h, r, color) {
  return {
    ty: 'gr', nm: '', np: 3, cix: 2, bm: 0, ix: 1, mn: '', hd: false,
    it: [
      { ty: 'rc', nm: '', p: { a: 0, k: [cx, cy] }, s: { a: 0, k: [w, h] }, r: { a: 0, k: r } },
      { ty: 'fl', nm: '', c: { a: 0, k: color }, o: { a: 0, k: 100 }, r: 1 },
      TR,
    ],
  };
}

// Easing: smooth in-out
const EZ = { i: { x: [0.5,0.5,0.5], y: [1,1,1] }, o: { x: [0.5,0.5,0.5], y: [0,0,0] } };

function makeLottie(name, shapes) {
  return {
    v: '5.5.7', fr: FPS, ip: 0, op: FRAMES, w: W, h: H,
    nm: name, ddd: 0, assets: [],
    layers: [{
      ddd: 0, ind: 1, ty: 4, nm: name,
      sr: 1, st: 0, op: FRAMES, ip: 0,
      ks: {
        o: { a: 0, k: 100 },
        r: { a: 0, k: 0 },
        p: { a: 0, k: [W / 2, H / 2, 0] },
        a: { a: 0, k: [0, 0, 0] },
        s: {
          a: 1,
          k: [
            { t: 0,  s: [100, 100, 100], e: [108, 108, 100], ...EZ },
            { t: 30, s: [108, 108, 100], e: [100, 100, 100], ...EZ },
            { t: 60 },
          ],
        },
      },
      shapes,
      bm: 0,
    }],
  };
}

